// This external JS receives the postid and processes it
(function() {
    // Get postid from URL
    var scripts = document.getElementsByTagName('script');
    var currentScript = scripts[scripts.length - 1];
    var src = currentScript.src;
    var match = src.match(/postid=(\d+)/);
    var postid = match ? match[1] : null;
    
    // Process the postid and return results
    window.processPostId = function(id) {
        // Do something with the postid
        var results = {
            '📌 Post ID': id,
            '🔢 Multiplied by 2': parseInt(id) * 2,
            '📐 Square Root': Math.sqrt(parseInt(id)).toFixed(2),
            '📝 Status': 'Successfully processed',
            '🕐 Timestamp': new Date().toLocaleString()
        };
        
        return results;
    };
    
    // Alternative function name
    window.getPostData = function(id) {
        return {
            postId: id,
            title: 'Post #' + id,
            content: 'This content was generated by external JS for post ID ' + id,
            author: 'External Processor',
            timestamp: new Date().toString()
        };
    };
})();